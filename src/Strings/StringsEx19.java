    // Funcție pentru calcularea scorului unui cuvânt dat folosind scorul literelor
    public static int calculateScore(String word, int[] letterScores) {
        int score = 0;
        for (int i = 0; i < word.length(); i++) {
            char letter = word.charAt(i);//parcurg cuvantul litera cu litera
            if (letter >= 'A' && letter <= 'Z') {
                int indice = letter - 'A'; // aflu indicele literei în array
                score += letterScores[indice]; // adaug scorul literei la scorul total
            }
        }
        return score;
    }
