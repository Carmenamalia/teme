//1. Scrie un program care afiseaza numerele de la 1 la n, n fiind citit de la tastatura
public class WhileLoopsEx1 {
    public static void main(String[] args) {
        int n = 10;
        int i = 0;
        while (i <= n) {
            System.out.println(i);
            i++;
        }


    }
}
